// ─── Deduplication helpers ───────────────────────────────────────────────────

/// Recursively rename type references within a TsType.
fn rename_type_ref(ty: &mut TsType, from: &str, to: &str) {
    match ty {
        TsType::Named(name) if name == from => *name = to.to_string(),
        TsType::Array(inner) => rename_type_ref(inner, from, to),
        TsType::Nullable(inner) => rename_type_ref(inner, from, to),
        TsType::Record(k, v) => {
            rename_type_ref(k, from, to);
            rename_type_ref(v, from, to);
        }
        TsType::Tuple(elems) => {
            for e in elems {
                rename_type_ref(e, from, to);
            }
        }
        _ => {}
    }
}

/// Rename a type definition and all internal type references from `from` to `to`.
fn rename_type_in_def(def: &mut TsTypeDef, from: &str, to: &str) {
    match def {
        TsTypeDef::Interface(iface) => {
            if iface.name == from {
                iface.name = to.to_string();
            }
            for field in &mut iface.fields {
                rename_type_ref(&mut field.ts_type, from, to);
            }
        }
        TsTypeDef::StringUnion(su) => {
            if su.name == from {
                su.name = to.to_string();
            }
        }
        TsTypeDef::TaggedUnion(tu) => {
            if tu.name == from {
                tu.name = to.to_string();
            }
            for variant in &mut tu.variants {
                rename_type_ref(&mut variant.data_type, from, to);
            }
        }
        TsTypeDef::TypeAlias { name, target } => {
            if name == from {
                *name = to.to_string();
            }
            rename_type_ref(target, from, to);
        }
    }
}

/// Apply a set of renames to all defs in a slice.
fn apply_renames(defs: &mut [TsTypeDef], renames: &[(&str, &str)]) {
    for (from, to) in renames {
        for def in defs.iter_mut() {
            rename_type_in_def(def, from, to);
        }
    }
}

// ─── Combined output generation ─────────────────────────────────────────────

/// Reads ooxml-types source files and writes a combined ooxml-types.ts
/// to the generated/ directory.
///
/// Run: cargo test -p bridge-ts --test generate_ooxml_types -- generate_combined --nocapture
#[test]
fn generate_combined() {
    let base = format!(
        "{}/../../../file-io/ooxml/types/src",
        env!("CARGO_MANIFEST_DIR")
    );
    let config = ooxml_config();

    // Enumerate all source files from drawings/, charts/ sub-directories and
    // top-level modules. Exclude mod.rs (re-exports only) and tests.rs (no public types).
    let mut source_files = vec![
        // drawings sub-modules
        format!("{}/drawings/preset.rs", base),
        format!("{}/drawings/primitives.rs", base),
        format!("{}/drawings/fill.rs", base),
        format!("{}/drawings/line.rs", base),
        format!("{}/drawings/transform.rs", base),
        format!("{}/drawings/geometry.rs", base),
        format!("{}/drawings/properties.rs", base),
        format!("{}/drawings/style.rs", base),
        format!("{}/drawings/effects.rs", base),
        format!("{}/drawings/three_d.rs", base),
        format!("{}/drawings/table.rs", base),
        format!("{}/drawings/spreadsheet.rs", base),
        // charts sub-modules
        format!("{}/charts/properties.rs", base),
        format!("{}/charts/config.rs", base),
        format!("{}/charts/data.rs", base),
        format!("{}/charts/series.rs", base),
        format!("{}/charts/axis.rs", base),
        format!("{}/charts/document.rs", base),
        format!("{}/charts/style.rs", base),
    ];
    source_files.extend(
        drawings_color_source_files()
            .iter()
            .map(|path| format!("{base}/{path}")),
    );
    source_files.extend(
        drawings_text_source_files()
            .iter()
            .map(|path| format!("{base}/{path}")),
    );
    source_files.extend(
        charts_enum_source_files()
            .iter()
            .map(|path| format!("{base}/{path}")),
    );
    source_files.extend(
        print_source_files()
            .iter()
            .map(|path| format!("{base}/{path}")),
    );
    source_files.extend(
        theme_source_files()
            .iter()
            .map(|path| format!("{base}/{path}")),
    );

    // Per-file type renames to resolve duplicate identifiers across OOXML modules.
    // The drawings/ and charts/enums types are considered canonical; newly-added
    // source files (print.rs, charts/style.rs) get prefixed names.
    // Keys are path suffixes matched against each source file path.
    let per_file_renames: &[(&str, &[(&str, &str)])] = &[
        // print/ defines Orientation (Default/Portrait/Landscape) and PageSetup
        // which collide with charts/enums.rs Orientation (MinMax/MaxMin) and
        // charts/properties.rs PageSetup.
        ("print/enums.rs", &[("Orientation", "PrintOrientation")]),
        ("print/setup.rs", &[("PageSetup", "PrintPageSetup")]),
        // charts/style.rs defines ColorTransform (simple tuple variants) which
        // collides with drawings/color.rs ColorTransform (comprehensive tagged union).
        (
            "charts/style.rs",
            &[("ColorTransform", "ChartColorTransform")],
        ),
    ];

    let mut all_defs = Vec::new();
    all_defs.push(shape_preset_def());
    for path in &source_files {
        let source = std::fs::read_to_string(path)
            .unwrap_or_else(|e| panic!("Failed to read {}: {}", path, e));
        let mut defs = bridge_ts::parse_types(&source, &config).unwrap();
        let filename = path.rsplit('/').next().unwrap();
        eprintln!("{}: {} types", filename, defs.len());

        // Apply per-file renames to avoid duplicate identifiers
        for (suffix, renames) in per_file_renames {
            if path.ends_with(suffix) {
                apply_renames(&mut defs, renames);
            }
        }

        all_defs.extend(defs);
    }

    let ts = bridge_ts::emit_type_defs(&all_defs, None);
    eprintln!(
        "\nGenerated ooxml-types.ts: {} bytes, {} types",
        ts.len(),
        all_defs.len()
    );

    // Write to generated/ directory
    let output_dir = format!("{}/generated", env!("CARGO_MANIFEST_DIR"));
    std::fs::create_dir_all(&output_dir).unwrap();
    let output_path = format!("{}/ooxml-types.ts", output_dir);
    std::fs::write(&output_path, &ts).unwrap();
    eprintln!("Written to: {}", output_path);
}
