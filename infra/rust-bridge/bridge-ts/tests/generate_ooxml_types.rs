//! Integration test: generate TypeScript types from ooxml-types source files.
//!
//! Reads real Rust source files from file-io/ooxml/types/src/ and generates
//! TypeScript type definitions. The ooxml-types crate has sub-module directories
//! (drawings/, charts/) so we enumerate all .rs files within them.

use bridge_ts::types::{TsType, TsTypeDef};
use bridge_ts::{TypeGenConfig, emit_type_defs, generate_types_from_source};
use std::collections::HashMap;

fn ooxml_config() -> TypeGenConfig {
    let mut map = HashMap::new();
    // Emu is `pub type Emu = i64` — a measurement unit alias that maps to number in TS
    map.insert("Emu".to_string(), bridge_ts::types::TsType::Number);
    // GeomCoord is `pub type GeomCoord = String` — a coordinate value represented as a string formula
    map.insert("GeomCoord".to_string(), bridge_ts::types::TsType::String);
    // TitleText is `pub type TitleText = ChartText` — alias for ChartText
    map.insert(
        "TitleText".to_string(),
        bridge_ts::types::TsType::Named("ChartText".into()),
    );

    // ── Macro-generated newtypes from drawings/primitives.rs ─────────────
    // These are generated by ooxml_newtype_int! / ooxml_newtype_str! macros
    // and are invisible to the syn parser, so we register them manually.

    // Integer newtypes (ooxml_newtype_int!) → number
    for name in &[
        // Coordinate types
        "StCoordinate",
        "StCoordinateUnqualified",
        "StCoordinate32",
        "StCoordinate32Unqualified",
        "StPositiveCoordinate",
        "StPositiveCoordinate32",
        "StLineWidth",
        // Angle types
        "StAngle",
        "StFixedAngle",
        "StPositiveFixedAngle",
        "StFovAngle",
        // Percentage types
        "StPercentage",
        "StPercentageDecimal",
        "StFixedPercentage",
        "StFixedPercentageDecimal",
        "StPositivePercentage",
        "StPositivePercentageDecimal",
        "StPositiveFixedPercentage",
        "StPositiveFixedPercentageDecimal",
        // Text measurement types
        "StTextPoint",
        "StTextPointUnqualified",
        "StTextNonNegativePoint",
        "StTextFontSize",
        "StTextMargin",
        "StTextIndent",
        "StTextIndentLevelType",
        "StTextColumnCount",
        "StTextFontScalePercent",
        "StTextFontScalePercentOrPercentString",
        // Text spacing types
        "StTextSpacingPercent",
        "StTextSpacingPercentOrPercentString",
        "StTextSpacingPoint",
        // Bullet types
        "StTextBulletSize",
        "StTextBulletSizeDecimal",
        "StTextBulletSizePercent",
        "StTextBulletStartAtNum",
        // Identifier types
        "StDrawingElementId",
        "StStyleMatrixColumnIndex",
        "StPitchFamily",
    ] {
        map.insert(name.to_string(), bridge_ts::types::TsType::Number);
    }

    // String newtypes (ooxml_newtype_str!) → string
    for name in &[
        "StTextTypeface",
        "StGeomGuideName",
        "StGeomGuideFormula",
        "StAdjAngle",
        "StAdjCoordinate",
        "StShapeId",
    ] {
        map.insert(name.to_string(), bridge_ts::types::TsType::String);
    }

    TypeGenConfig {
        external_type_map: map,
        ..Default::default()
    }
}

fn read_ooxml_source(path_from_types: &str) -> String {
    let path = format!(
        "{}/../../../file-io/ooxml/types/src/{}",
        env!("CARGO_MANIFEST_DIR"),
        path_from_types
    );
    std::fs::read_to_string(&path).unwrap_or_else(|e| panic!("Failed to read {}: {}", path, e))
}

fn read_ooxml_sources(paths_from_types: &[&str]) -> String {
    paths_from_types
        .iter()
        .map(|path| read_ooxml_source(path))
        .collect::<Vec<_>>()
        .join("\n")
}

